def generate_tarot_seed(question: str, date_str: str, spread_type: str) -> int:
    """타로 리딩을 위한 시드 생성"""
    seed_str = f"{question}_{date_str}_{spread_type}"
    return int(hashlib.md5(seed_str.encode()).hexdigest(), 16)


def get_all_cards() -> List[Dict[str, Any]]:
    """전체 78장 카드 리스트 반환 (메이저 22장 + 마이너 56장)"""
    all_cards = []

    # 메이저 아르카나 22장 (인덱스 0-21)
    for i in range(22):
        card = MAJOR_ARCANA[i].copy()
        card['card_type'] = 'major'
        card['global_index'] = i
        all_cards.append(card)

    # 마이너 아르카나 56장 (인덱스 22-77)
    global_idx = 22
    for suit_name in ['wands', 'cups', 'swords', 'pentacles']:
        suit = MINOR_ARCANA[suit_name]
        # 에이스
        card = suit['ace'].copy()
        card['card_type'] = 'minor'
        card['global_index'] = global_idx
        all_cards.append(card)
        global_idx += 1
        # 2-10
        for num in range(2, 11):
            card = suit[num].copy()
            card['card_type'] = 'minor'
            card['global_index'] = global_idx
            all_cards.append(card)
            global_idx += 1
        # 코트 카드 (Page, Knight, Queen, King)
        for court in ['page', 'knight', 'queen', 'king']:
            card = suit[court].copy()
            card['card_type'] = 'minor'
            card['global_index'] = global_idx
            all_cards.append(card)
            global_idx += 1

    return all_cards


def draw_cards(count: int, seed: int, use_full_deck: bool = True) -> List[Dict[str, Any]]:
    """카드 뽑기

    Args:
        count: 뽑을 카드 수
        seed: 랜덤 시드
        use_full_deck: True면 78장, False면 메이저 22장만 사용

    Returns:
        뽑힌 카드 리스트 (정방향/역방향 포함)
    """
    random.seed(seed)

    if use_full_deck:
        all_cards = get_all_cards()
        selected_cards = random.sample(all_cards, count)
    else:
        # 메이저 아르카나만 사용
        card_indices = random.sample(range(22), count)
        selected_cards = [MAJOR_ARCANA[idx].copy() for idx in card_indices]

    result = []
    for card in selected_cards:
        card_copy = card.copy()
        # 정방향(True) 또는 역방향(False) 결정 (약 30% 확률로 역방향)
        is_upright = random.random() > 0.3
        card_copy['is_upright'] = is_upright
        card_copy['orientation'] = '정방향' if is_upright else '역방향'
        result.append(card_copy)

    return result


def draw_selected_cards(selected_indices: List[int], use_full_deck: bool = True) -> List[Dict[str, Any]]:
    """사용자가 선택한 카드 뽑기

    Args:
        selected_indices: 사용자가 선택한 카드 인덱스 리스트 (0-77 for full deck, 0-21 for major only)
        use_full_deck: True면 78장, False면 메이저 22장만 사용

    Returns:
        선택된 카드 리스트 (정방향/역방향 포함)
    """
    if use_full_deck:
        all_cards = get_all_cards()
        max_idx = 78
    else:
        all_cards = None
        max_idx = 22

    result = []
    for idx in selected_indices:
        if 0 <= idx < max_idx:
            if use_full_deck:
                card = all_cards[idx].copy()
            else:
                card = MAJOR_ARCANA[idx].copy()
            # 정방향(True) 또는 역방향(False) 결정 (약 30% 확률로 역방향)
            is_upright = random.random() > 0.3
            card['is_upright'] = is_upright
            card['orientation'] = '정방향' if is_upright else '역방향'
            result.append(card)

    return result


def get_card_reading(card: Dict[str, Any], position: str) -> Dict[str, Any]:
    """카드 해석 가져오기

    Args:
        card: 카드 정보
        position: 스프레드에서의 위치

    Returns:
        해석 정보
    """
    reading_type = 'upright' if card['is_upright'] else 'reversed'
    reading = card[reading_type]

    return {
        'number': card['number'],
        'name': card['name'],
        'name_kr': card['name_kr'],
        'symbol': card['symbol'],
        'keywords': card['keywords'],
        'position': position,
        'orientation': card['orientation'],
        'is_upright': card['is_upright'],
        'meaning': reading['meaning'],
        'love': reading['love'],
        'money': reading['money'],
        'work': reading['work'],
        'advice': reading['advice']
    }


def get_tarot_reading(spread_type: str = 'one_card',
                      question: str = '',
                      target_date: Optional[datetime] = None,
                      selected_cards: Optional[List[int]] = None) -> Dict[str, Any]:
    """타로 리딩 수행

    Args:
        spread_type: 스프레드 타입 ('one_card', 'three_card', 'love_spread', 'yes_no')
        question: 질문 (선택)
        target_date: 리딩 날짜 (기본: 오늘)
        selected_cards: 사용자가 선택한 카드 인덱스 목록 (선택)

    Returns:
        타로 리딩 결과
    """
    if target_date is None:
        target_date = datetime.now()

    date_str = target_date.strftime('%Y-%m-%d')

    # 스프레드 정보 가져오기
    spread = SPREAD_TYPES.get(spread_type, SPREAD_TYPES['one_card'])

    # 사용자가 카드를 선택한 경우 해당 카드 사용
    if selected_cards and len(selected_cards) >= spread['card_count']:
        cards = draw_selected_cards(selected_cards[:spread['card_count']])
    else:
        # 기존 랜덤 방식
        seed = generate_tarot_seed(question, date_str, spread_type)
        cards = draw_cards(spread['card_count'], seed)

    # 각 카드에 대한 해석 생성
    readings = []
    for i, card in enumerate(cards):
        position = spread['positions'][i]
        reading = get_card_reading(card, position)
        readings.append(reading)

    # 예/아니오 스프레드인 경우 추가 해석
    yes_no_answer = None
    if spread_type == 'yes_no' and readings:
        card = readings[0]
        # 정방향 긍정카드면 Yes, 역방향이거나 부정카드면 No
        positive_cards = [0, 1, 3, 6, 7, 8, 10, 11, 14, 17, 19, 21]  # 긍정적인 카드들
        is_positive = card['number'] in positive_cards
        if card['is_upright'] and is_positive:
            yes_no_answer = 'YES'
        elif not card['is_upright'] and not is_positive:
            yes_no_answer = 'YES (주의 필요)'
        else:
            yes_no_answer = 'NO' if not card['is_upright'] else '불확실'

    # 종합 메시지 생성
    if len(readings) == 1:
        overall_message = readings[0]['advice']
    elif len(readings) == 3:
        overall_message = f"과거의 {readings[0]['name_kr']}에서 현재의 {readings[1]['name_kr']}를 거쳐 미래의 {readings[2]['name_kr']}로 향하고 있습니다. {readings[2]['advice']}"
    else:
        overall_message = "카드가 당신에게 메시지를 전합니다."

    # 프론트엔드용 카드 데이터 포맷팅
    formatted_cards = []
    for reading in readings:
        formatted_cards.append({
            'number': reading['number'],
            'name': reading['name'],
            'name_kr': reading['name_kr'],
            'symbol': reading['symbol'],
            'keywords': reading['keywords'],
            'position': reading['position'],
            'orientation': reading['orientation'],
            'is_reversed': not reading['is_upright'],
            'reading': {
                'meaning': reading['meaning'],
                'love': reading['love'],
                'money': reading['money'],
                'work': reading['work'],
                'advice': reading['advice']
            }
        })

    return {
        'spread_type': spread_type,
        'spread': {
            'name': spread['name'],
            'description': spread['description']
        },
        'question': question,
        'date': date_str,
        'cards': formatted_cards,
        'yes_no_answer': yes_no_answer,
        'overall_message': overall_message,
        'card_count': len(readings)
    }


